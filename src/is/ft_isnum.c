#include "libft.h"

int	ft_isnum(char *str)
{
	if (*str == '+' || *str == '-')
		str++;
	if (!(*str))
		return (0);
	while (*str)
	{
		if (*str >= '0' && *str <= '9')
			str++;
		else
			return (0);
	}
	return (1);
}
